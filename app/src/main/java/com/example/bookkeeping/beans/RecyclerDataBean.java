package com.example.bookkeeping.beans;

public class RecyclerDataBean {
    private String name;
    private int img;

    public RecyclerDataBean(String name, int img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
